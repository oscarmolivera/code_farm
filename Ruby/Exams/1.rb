def fizzBuzz(n)
  (1..n).each  do |num|
    puts "FizzBuzz" if num % 3 == 0 && num % 5 == 0
    puts "Fizz" if num % 3 == 0 && num % 5 != 0
    puts "Buzz" if num % 3 != 0 && num % 5 == 0
    puts "#{num}" if num % 3 != 0 && num % 5 != 0
  end
end

fizzBuzz(15)
