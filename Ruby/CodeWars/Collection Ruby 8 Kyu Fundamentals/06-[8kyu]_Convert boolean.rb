=begin 
 Convert boolean values to strings 'Yes' or 'No'.
 Complete the method that takes a boolean value and return a "Yes"
 string for true, or a "No" string for false.
=end

def bool_to_word(bool)
  bool ? 'Yes' : 'No'
end

p bool_to_word(true)
# => 'Yes'
p bool_to_word(false)
# => 'No'

