#!/usr/bin/env python

print("***** Operaciones de Suma Resta Multiplicacion y Division! *****")
num1 = input("Ingrese el primer numero:")
num2 = input("Ingrese el segundo numero:")
sma = float(num1) + float(num2)
res = float(num1) - float(num2)
mul = float(num1) * float(num2)
div = float(num1) / float(num2)
print("Suma:%.2f, Resta:%.2f, Multiplicación:%.2f, División:%.2f"%(sma, res, mul, div))